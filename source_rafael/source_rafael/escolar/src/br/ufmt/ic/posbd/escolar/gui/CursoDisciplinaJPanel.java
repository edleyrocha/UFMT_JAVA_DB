    private void excluirjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirjButtonActionPerformed
        // TODO add your handling code here:
        int[] linhas = tabelajTable.getSelectedRows();
        if (JOptionPane.showConfirmDialog(excluirjButton, "Deseja realmente excluir?") == JOptionPane.YES_OPTION) {
            for (int i = 0; i < linhas.length; i++) {
                CursoDisciplina cursoDisciplina = tableModel.get(linhas[i]);
                dao.excluir(cursoDisciplina.getId());
            }
            JOptionPane.showMessageDialog(tabelajTable, "Removidos com Sucesso!");
            tableModel.atualizar(dao.listar());
        }
    }//GEN-LAST:event_excluirjButtonActionPerformed

    private void salvarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarjButtonActionPerformed
        // TODO add your handling code here:
        if (cursojComboBox.getSelectedIndex() > 0) {
            CursoDisciplina cursoDisciplina = new CursoDisciplina();

            cursoDisciplina.setCurso((Curso) cursojComboBox.getSelectedItem());
            cursoDisciplina.setDisciplina((Disciplina) disciplinajComboBox.getSelectedItem());

            if (dao.exists(cursoDisciplina.getCurso(), cursoDisciplina.getDisciplina())) {
                JOptionPane.showMessageDialog(salvarjButton, "JÃ¡ existe disciplina neste curso!");
            } else {
                dao.inserir(cursoDisciplina);
                JOptionPane.showMessageDialog(salvarjButton, "Inserido com Sucesso!");
                tableModel.atualizar(dao.listar());
                cursojComboBox.setSelectedIndex(0);
                disciplinajComboBox.setSelectedIndex(0);
            }
        } else {
            JOptionPane.showMessageDialog(cursojComboBox, "Selecione o curso");
            cursojComboBox.grabFocus();
        }
    }//GEN-LAST:event_salvarjButtonActionPerformed


