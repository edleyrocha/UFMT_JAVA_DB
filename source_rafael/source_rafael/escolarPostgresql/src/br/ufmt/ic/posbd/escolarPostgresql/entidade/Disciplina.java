package br.ufmt.ic.posbd.escolarPostgresql.entidade;

import br.ufmt.ic.posbd.escolarMysql.entidade.ICRUD;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;
import javax.persistence.Table;

/**
 *
 * @author raphael
 */
@Entity
@Table(name = "disciplina")
@SequenceGenerator(allocationSize = 1,
        sequenceName = "disciplina_id_seq", name = "seqDisciplina")
public class Disciplina implements ICRUD {

    @Id
    @GeneratedValue(generator = "seqDisciplina",
            strategy = GenerationType.SEQUENCE)
    private int id;
    private String nome;
    private int cargaHoraria;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getCargaHoraria() {
        return cargaHoraria;
    }

    public void setCargaHoraria(int cargaHoraria) {
        this.cargaHoraria = cargaHoraria;
    }

    @Override
    public String toString() {
        return nome;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 89 * hash + this.id;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Disciplina other = (Disciplina) obj;
        if (this.id != other.id) {
            return false;
        }
        return true;
    }
    
    

}
